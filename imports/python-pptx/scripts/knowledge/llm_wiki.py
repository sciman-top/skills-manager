# Query: ask questions against the wiki
# ──────────────────────────────────────────────────────


def cmd_query(args: argparse.Namespace) -> int:
    """Query the wiki by question.

    Reads index.md to find relevant pages, then synthesizes an answer.
    For a CLI tool without LLM access, this performs a keyword search
    across wiki pages and returns matching results.
    """
    domain = args.wiki
    wiki_root = WIKIS_DIR / domain

    if not wiki_root.exists():
        print(f"[ERROR] Wiki not found: {domain}")
        return 1

    query_text = " ".join(args.question).lower()
    query_words = [w for w in query_text.split() if len(w) > 2]

    if not query_words:
        print("[ERROR] Query too short. Provide meaningful keywords.")
        return 1

    # Search across wiki markdown files
    results = []
    wiki_dir = wiki_root / "wiki"

    for category in ["entities", "concepts", "sources", "comparisons"]:
        subdir = wiki_dir / category
        if not subdir.exists():
            continue

        for md_file in subdir.glob("*.md"):
            content = md_file.read_text()
            matches = sum(1 for word in query_words if word in content.lower())
            if matches > 0:
                results.append({
                    "path": str(md_file.relative_to(REPO_ROOT)),
                    "matches": matches,
                    "content_snippet": content[:300],
                })

    # Sort by relevance
    results.sort(key=lambda x: x["matches"], reverse=True)

    print(f"=== Query: {' '.join(args.question)} ===")
    print(f"    Wiki: {domain}")
    print(f"    Found {len(results)} matching pages")
    print()

    if not results:
        print("No matching pages found.")
        print()
        print("Possible actions:")
        print(f"  - Ingest more sources: ingest <file> --wiki {domain}")
        print(f"  - Check index: status --wiki {domain}")
        return 0

    for i, result in enumerate(results, 1):
        print(f"[{i}] {result['path']}")
        print(f"    Matches: {result['matches']} keywords")
        snippet = result["content_snippet"].strip()
        # Show first non-frontmatter line
        for line in snippet.splitlines():
            line = line.strip()
            if line and not line.startswith("---") and not line.startswith(">:") and not line.startswith("created:") and not line.startswith("last_updated:"):
                print(f"    {line}")
                break
        print()

    if args.file_back:
        output_path = wiki_root / "wiki" / "comparisons" / f"q_auto.md"
        print(f"Would save query results to: {output_path.relative_to(REPO_ROOT)}")

    return 0


