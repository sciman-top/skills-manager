def main():
    parser = argparse.ArgumentParser(
        prog="llm-wiki",
        description="LLM Wiki - Build and maintain persistent knowledge bases using LLMs",
        epilog="See https://gist.github.com/karpathy/442a6bf555914893e9891c11519de94f",
    )
    subparsers = parser.add_subparsers(dest="command", help="Command to run")

    # init
    init_p = subparsers.add_parser("init", help="Scaffold a new domain wiki")
    init_p.add_argument("domain", help="Domain name (e.g., marine-engineering)")

    # status
    status_p = subparsers.add_parser("status", help="Report wiki statistics")
    status_p.add_argument("--wiki", "-w", required=True, help="Wiki domain name")

    # ingest
    ingest_p = subparsers.add_parser("ingest", help="Ingest a source file into the wiki")
    ingest_p.add_argument("file", help="Source file to ingest")
    ingest_p.add_argument("--wiki", "-w", required=True, help="Wiki domain name")
    ingest_p.add_argument("--force", "-f", action="store_true", help="Overwrite existing")

    # query
    query_p = subparsers.add_parser("query", help="Query the wiki")
    query_p.add_argument("question", nargs="*", help="Question to ask")
    query_p.add_argument("--wiki", "-w", required=True, help="Wiki domain name")
    query_p.add_argument("--file-back", action="store_true", help="Save answer to wiki")

    # lint
    lint_p = subparsers.add_parser("lint", help="Health-check the wiki")
    lint_p.add_argument("--wiki", "-w", required=True, help="Wiki domain name")

    # batch-ingest
    batch_p = subparsers.add_parser(
        "batch-ingest",
        help="Bulk-create wiki source pages from a metadata file (JSONL/JSON/YAML)",
    )
    batch_p.add_argument(
        "metadata_file",
        help="Path to metadata file (.jsonl, .json, .yaml, or .yml)",
    )
    batch_p.add_argument("--wiki", "-w", required=True, help="Wiki domain name")
    batch_p.add_argument(
        "--batch-size",
        type=int,
        default=50,
        metavar="N",
        help="Number of records per batch (default: 50)",
    )
    batch_p.add_argument(
        "--dry-run",
        action="store_true",
        help="Preview what would be created without writing any files",
    )

    args = parser.parse_args()

    if not args.command:
        parser.print_help()
        return 1

    commands = {
        "init": cmd_init,
        "status": cmd_status,
        "ingest": cmd_ingest,
        "query": cmd_query,
        "lint": cmd_lint,
        "batch-ingest": cmd_batch_ingest,
    }

    handler = commands.get(args.command)
    if handler:
        return handler(args)
    else:
        parser.print_help()
        return 1


if __name__ == "__main__":
    sys.exit(main())
