class TestJumperNaturalFrequency:
    """Natural frequency for 6" jumper example from MATLAB defaults."""

    def test_fn_positive(self, jumper_input):
        calc = SpanNaturalFrequency(jumper_input)
        assert calc.fn_IL() > 0
        assert calc.fn_CF() > 0

    def test_fn_order_of_magnitude(self, jumper_input):
        """6" pipe, L=30 m → fn should be roughly 0.5-3 Hz range."""
        calc = SpanNaturalFrequency(jumper_input)
        fn = calc.fn_IL()
        assert 0.1 < fn < 10.0, f"fn_IL={fn:.3f} Hz seems out of range"

    def test_seabed_gap_large_gives_ca_one(self, jumper_input):
        """e/D = 2.63/0.1683 = 15.6 >> 0.8 → Ca = 1.0 (no proximity effect)."""
        e_over_D = jumper_input.seabed_gap_m / jumper_input.od_m
        assert e_over_D > 0.8
        ca = _added_mass_coefficient(e_over_D)
        assert ca == 1.0

    def test_effective_mass_includes_contents(self, jumper_input):
        """Effective mass includes pipe steel + contents + added mass."""
        calc = SpanNaturalFrequency(jumper_input)
        result = calc.compute()
        # Steel area
        ri = (jumper_input.od_m - 2 * jumper_input.wt_m) / 2
        ro = jumper_input.od_m / 2
        A_steel = math.pi * (ro**2 - ri**2)
        m_steel = A_steel * jumper_input.steel_density_kgm3
        # Contents area
        A_contents = math.pi * ri**2
        m_contents = A_contents * jumper_input.content_density_kgm3
        # Effective mass must exceed dry mass
        assert result.m_effective_kgm > m_steel + m_contents


