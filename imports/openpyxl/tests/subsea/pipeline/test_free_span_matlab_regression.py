class TestReductionFactors:
    """Turbulence intensity and flow angle reduction factors."""

    def test_reduction_factor_r1_default_turbulence(self):
        """R1 at Ic=0.05, theta=90° → R1 = 1 - π²(0.05-0.03)(π/2 - √2·π/2).

        From MATLAB ObtainParams:
            reduction1 = 1 - π²*(Ic - 0.03)*(π/2 - √2*theta_rel)
        At theta_rel = π/2 (90°):
            π/2 - √2·π/2 = π/2(1 - √2) ≈ -0.6685
            R1 = 1 - π²·0.02·(-0.6685) ≈ 1 + 0.1319 → clamp to 1.0
        """
        Ic = 0.05
        theta = math.pi / 2
        R1 = 1.0 - math.pi**2 * (Ic - 0.03) * (math.pi / 2 - math.sqrt(2) * theta)
        R1 = max(0.0, min(1.0, R1))  # clamp [0, 1]
        assert 0.0 <= R1 <= 1.0

    def test_reduction_factor_r2_default_turbulence(self):
        """R2 at Ic=0.05: R2 = 1 - (0.05 - 0.03)/0.17 ≈ 0.882."""
        Ic = 0.05
        R2 = 1.0 - (Ic - 0.03) / 0.17
        R2 = max(0.0, min(1.0, R2))
        expected = 1.0 - 0.02 / 0.17
        assert abs(R2 - expected) < 1e-6
        assert 0.8 < R2 < 1.0
