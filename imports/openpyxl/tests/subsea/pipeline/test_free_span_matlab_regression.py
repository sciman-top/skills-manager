class TestWeibullCurrentFit:
    """Tests for Weibull current profile fitting (DNV-RP-F105 Sec 3.3)."""

    def test_fit_returns_positive_shape_parameters(self, matlab_current_profile):
        """Weibull shape k > 0 for well-behaved current data."""
        speeds = matlab_current_profile["speeds"]
        occ = matlab_current_profile["occurrence_probs"]
        # Convert occurrence to exceedance
        import numpy as np
        cum = np.cumsum(occ)
        exc = list(np.round(1e8 * (1 - cum)) / 1e8)  # match MATLAB rounding

        fit = fit_weibull_current(exc, speeds)
        assert fit.k_upper > 0, f"k_upper={fit.k_upper} should be > 0"
        assert fit.k_lower > 0, f"k_lower={fit.k_lower} should be > 0"

    def test_100yr_current_exceeds_max_observed(self, matlab_current_profile):
        """100-year return period current > max observed (1.0 m/s)."""
        speeds = matlab_current_profile["speeds"]
        occ = matlab_current_profile["occurrence_probs"]
        import numpy as np
        cum = np.cumsum(occ)
        exc = list(np.round(1e8 * (1 - cum)) / 1e8)

        # 100-year exceedance probability for 24-hr duration
        T_duration = 24  # hours
        p_100yr = T_duration / (24 * 365.25 * 100)

        u_100yr = extrapolate_current(exc, speeds, p_100yr)
        assert u_100yr > 1.0, (
            f"100-yr current {u_100yr:.3f} should exceed max observed 1.0 m/s"
        )

    def test_10yr_between_1yr_and_100yr(self, matlab_current_profile):
        """10-year return current is between 1-year and 100-year."""
        speeds = matlab_current_profile["speeds"]
        occ = matlab_current_profile["occurrence_probs"]
        import numpy as np
        cum = np.cumsum(occ)
        exc = list(np.round(1e8 * (1 - cum)) / 1e8)

        T_duration = 24
        p_1yr = T_duration / (24 * 365.25 * 1)
        p_10yr = T_duration / (24 * 365.25 * 10)
        p_100yr = T_duration / (24 * 365.25 * 100)

        u_1yr, u_10yr, u_100yr = extrapolate_current(
            exc, speeds, [p_1yr, p_10yr, p_100yr]
        )
        assert u_1yr < u_10yr < u_100yr

    def test_extrapolation_monotonic(self, matlab_current_profile):
        """Current speed increases monotonically as exceedance decreases."""
        speeds = matlab_current_profile["speeds"]
        occ = matlab_current_profile["occurrence_probs"]
        import numpy as np
        cum = np.cumsum(occ)
        exc = list(np.round(1e8 * (1 - cum)) / 1e8)

        probs = [0.15, 0.10, 0.05, 0.01, 0.005, 0.001]
        results = extrapolate_current(exc, speeds, probs)
        for i in range(len(results) - 1):
            assert results[i] <= results[i + 1], (
                f"Non-monotonic: U({probs[i]})={results[i]:.3f} > "
                f"U({probs[i+1]})={results[i+1]:.3f}"
            )

    def test_known_points_reproduced(self, matlab_current_profile):
        """Extrapolating at a known data point reproduces the input speed."""
        speeds = matlab_current_profile["speeds"]
        occ = matlab_current_profile["occurrence_probs"]
        import numpy as np
        cum = np.cumsum(occ)
        exc = list(np.round(1e8 * (1 - cum)) / 1e8)

        # The highest-speed data point should be reproduced exactly
        # p_exc for 1.0 m/s is exc[-1] (should be ~0.0)
        # Use the second-highest instead (exc[-2] = 0.01)
        u_at_p01 = extrapolate_current(exc, speeds, exc[-2])
        assert abs(u_at_p01 - speeds[-2]) < 0.05, (
            f"At P_exc={exc[-2]}: got {u_at_p01:.3f}, expected ~{speeds[-2]}"
        )

    def test_two_point_minimum(self):
        """Weibull fit works with exactly 2 data points."""
        speeds = [0.5, 1.0]
        exc = [0.10, 0.01]
        fit = fit_weibull_current(exc, speeds)
        assert fit.k_upper != 0
        u = extrapolate_current(exc, speeds, 0.001)
        assert u > 1.0

    def test_raises_on_single_point(self):
        """Must have ≥ 2 points."""
        with pytest.raises(ValueError, match="At least 2"):
            fit_weibull_current([0.1], [0.5])

    def test_raises_on_length_mismatch(self):
        """Lengths of probs and speeds must match."""
        with pytest.raises(ValueError, match="Length mismatch"):
            fit_weibull_current([0.1, 0.01], [0.5])


